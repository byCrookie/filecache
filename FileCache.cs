using System;
using System.Data.Common;
using System.IO;
using System.Threading;
using System.Threading.Tasks;
using Microsoft.Data.Sqlite;
using Microsoft.Extensions.Caching.Distributed;

namespace FileCache
{
    /*
       https://github.com/byCrookie/filecache

       Simple SQLite-based implementation of IDistributedCache.
       Copy this file into your project if you need a lightweight, file-backed cache.

       Requirements:
         - Microsoft.Data.Sqlite
         - Microsoft.Extensions.Caching.Abstractions

       Works with:
         - C# 9.0+
         - .NET Standard 2.1

       No setup, no extras â€” just drop it in and use.
    */

    public class FileCache : IDistributedCache
    {
        private readonly string _dbPath;
        private readonly string _connectionString;
        private readonly Func<DateTimeOffset> _utcNow;
        private readonly Lazy<bool> _init;

        public class FileCacheOptions
        {
            public string FilePath { get; set; } = "cache.db";
            public Func<DateTimeOffset>? UtcNow { get; set; }
        }

        public FileCache(FileCacheOptions? options = null)
        {
            var finalOptions = options ?? new FileCacheOptions();

            if (string.IsNullOrWhiteSpace(finalOptions.FilePath))
            {
                throw new ArgumentException("A non-empty file path is required.", nameof(options));
            }

            _dbPath = finalOptions.FilePath;
            _connectionString = $"Data Source={_dbPath}";
            _utcNow = finalOptions.UtcNow ?? (() => DateTimeOffset.UtcNow);
            _init = new Lazy<bool>(Initialize);

            _ = _init.Value;
        }

        private bool Initialize()
        {
            var directory = Path.GetDirectoryName(_dbPath);
            if (!string.IsNullOrEmpty(directory))
            {
                Directory.CreateDirectory(directory);
            }

            using var conn = CreateConnection();
            conn.Open();
            using var cmd = conn.CreateCommand();
            cmd.CommandText =
                @"CREATE TABLE IF NOT EXISTS cache (
                    key TEXT PRIMARY KEY,
                    value BLOB NOT NULL,
                    absolute_expiration INTEGER,
                    sliding_expiration INTEGER,
                    last_access INTEGER NOT NULL
                )";
            cmd.ExecuteNonQuery();
            return true;
        }

        private void EnsureInitialized()
        {
            _ = _init.Value;
        }

        private SqliteConnection CreateConnection() => new(_connectionString);

        private long NowUnix() => _utcNow().ToUnixTimeSeconds();

        private static void ValidateKey(string key)
        {
            if (string.IsNullOrEmpty(key))
            {
                throw new ArgumentException("Key cannot be null or empty.", nameof(key));
            }
        }

        private static void ValidateValue(byte[]? value)
        {
            if (value is null)
            {
                throw new ArgumentNullException(nameof(value));
            }
        }

        private static void ValidateOptions(DistributedCacheEntryOptions options)
        {
            if (options is null)
            {
                throw new ArgumentNullException(nameof(options));
            }

            if (options.SlidingExpiration.HasValue && options.SlidingExpiration <= TimeSpan.Zero)
            {
                throw new ArgumentOutOfRangeException(
                    nameof(options.SlidingExpiration),
                    "Sliding expiration must be positive."
                );
            }

            if (
                options.AbsoluteExpirationRelativeToNow.HasValue
                && options.AbsoluteExpirationRelativeToNow <= TimeSpan.Zero
            )
            {
                throw new ArgumentOutOfRangeException(
                    nameof(options.AbsoluteExpirationRelativeToNow),
                    "Absolute expiration relative to now must be positive."
                );
            }
        }

        public byte[]? Get(string key)
        {
            EnsureInitialized();
            ValidateKey(key);

            using var conn = CreateConnection();
            conn.Open();
            using var tx = conn.BeginTransaction();

            var record = ReadEntry(conn, key, tx);
            if (!record.HasValue)
            {
                tx.Commit();
                return null;
            }

            var entry = record.Value;
            var value = entry.Value;
            var absExp = entry.AbsoluteExpiration;
            var slidingExp = entry.SlidingExpiration;
            var lastAccess = entry.LastAccess;
            var now = NowUnix();
            if (IsExpired(now, absExp, slidingExp, lastAccess))
            {
                DeleteEntry(conn, key, tx);
                tx.Commit();
                return null;
            }

            if (slidingExp.HasValue)
            {
                UpdateLastAccess(conn, key, now, tx);
            }

            tx.Commit();
            return value;
        }

        public async Task<byte[]?> GetAsync(string key, CancellationToken token = default)
        {
            EnsureInitialized();
            ValidateKey(key);

            await using var conn = CreateConnection();
            await conn.OpenAsync(token);
            await using var tx = await conn.BeginTransactionAsync(token);

            var record = await ReadEntryAsync(conn, key, tx, token);
            if (!record.HasValue)
            {
                await tx.CommitAsync(token);
                return null;
            }

            var (value, absExp, slidingExp, lastAccess) = record.Value;
            var now = NowUnix();
            if (IsExpired(now, absExp, slidingExp, lastAccess))
            {
                await DeleteEntryAsync(conn, key, tx, token);
                await tx.CommitAsync(token);
                return null;
            }

            if (slidingExp.HasValue)
            {
                await UpdateLastAccessAsync(conn, key, now, tx, token);
            }

            await tx.CommitAsync(token);
            return value;
        }

        public void Set(string key, byte[] value, DistributedCacheEntryOptions options)
        {
            EnsureInitialized();
            ValidateKey(key);
            ValidateValue(value);
            ValidateOptions(options);

            var (now, absExp, slidingExp) = CalculateExpiration(options);

            using var conn = CreateConnection();
            conn.Open();
            using var cmd = conn.CreateCommand();
            cmd.CommandText =
                @"INSERT INTO cache (key, value, absolute_expiration, sliding_expiration, last_access)
                VALUES (@key, @value, @absExp, @slidingExp, @now)
                ON CONFLICT(key) DO UPDATE SET value = excluded.value, absolute_expiration = excluded.absolute_expiration, sliding_expiration = excluded.sliding_expiration, last_access = excluded.last_access";
            cmd.Parameters.AddWithValue("@key", key);
            cmd.Parameters.AddWithValue("@value", value);
            cmd.Parameters.AddWithValue("@absExp", (object?)absExp ?? DBNull.Value);
            cmd.Parameters.AddWithValue("@slidingExp", (object?)slidingExp ?? DBNull.Value);
            cmd.Parameters.AddWithValue("@now", now);
            cmd.ExecuteNonQuery();
        }

        public async Task SetAsync(
            string key,
            byte[] value,
            DistributedCacheEntryOptions options,
            CancellationToken token = default
        )
        {
            EnsureInitialized();
            ValidateKey(key);
            ValidateValue(value);
            ValidateOptions(options);

            var (now, absExp, slidingExp) = CalculateExpiration(options);

            await using var conn = CreateConnection();
            await conn.OpenAsync(token);
            await using var cmd = conn.CreateCommand();
            cmd.CommandText =
                @"INSERT INTO cache (key, value, absolute_expiration, sliding_expiration, last_access)
                VALUES (@key, @value, @absExp, @slidingExp, @now)
                ON CONFLICT(key) DO UPDATE SET value = excluded.value, absolute_expiration = excluded.absolute_expiration, sliding_expiration = excluded.sliding_expiration, last_access = excluded.last_access";
            cmd.Parameters.AddWithValue("@key", key);
            cmd.Parameters.AddWithValue("@value", value);
            cmd.Parameters.AddWithValue("@absExp", (object?)absExp ?? DBNull.Value);
            cmd.Parameters.AddWithValue("@slidingExp", (object?)slidingExp ?? DBNull.Value);
            cmd.Parameters.AddWithValue("@now", now);
            await cmd.ExecuteNonQueryAsync(token);
        }

        public void Refresh(string key)
        {
            EnsureInitialized();
            ValidateKey(key);

            using var conn = CreateConnection();
            conn.Open();
            using var cmd = conn.CreateCommand();
            cmd.CommandText = "SELECT sliding_expiration FROM cache WHERE key = @key";
            cmd.Parameters.AddWithValue("@key", key);
            var slidingExp = cmd.ExecuteScalar();
            if (slidingExp == DBNull.Value || slidingExp == null)
            {
                return;
            }

            using var updateCmd = conn.CreateCommand();
            updateCmd.CommandText = "UPDATE cache SET last_access = @now WHERE key = @key";
            updateCmd.Parameters.AddWithValue("@now", NowUnix());
            updateCmd.Parameters.AddWithValue("@key", key);
            updateCmd.ExecuteNonQuery();
        }

        public async Task RefreshAsync(string key, CancellationToken token = default)
        {
            EnsureInitialized();
            ValidateKey(key);

            await using var conn = CreateConnection();
            await conn.OpenAsync(token);
            await using var cmd = conn.CreateCommand();
            cmd.CommandText = "SELECT sliding_expiration FROM cache WHERE key = @key";
            cmd.Parameters.AddWithValue("@key", key);
            var slidingExp = await cmd.ExecuteScalarAsync(token);
            if (slidingExp == DBNull.Value || slidingExp == null)
            {
                return;
            }

            await using var updateCmd = conn.CreateCommand();
            updateCmd.CommandText = "UPDATE cache SET last_access = @now WHERE key = @key";
            updateCmd.Parameters.AddWithValue("@now", NowUnix());
            updateCmd.Parameters.AddWithValue("@key", key);
            await updateCmd.ExecuteNonQueryAsync(token);
        }

        public void Remove(string key)
        {
            EnsureInitialized();
            ValidateKey(key);

            using var conn = CreateConnection();
            conn.Open();
            DeleteEntry(conn, key, transaction: null);
        }

        public async Task RemoveAsync(string key, CancellationToken token = default)
        {
            EnsureInitialized();
            ValidateKey(key);

            await using var conn = CreateConnection();
            await conn.OpenAsync(token);
            await DeleteEntryAsync(conn, key, transaction: null, token);
        }

        private (long now, long? absoluteExpiration, long? slidingExpiration) CalculateExpiration(
            DistributedCacheEntryOptions options
        )
        {
            var now = NowUnix();
            var absoluteExpiration = options.AbsoluteExpiration?.ToUnixTimeSeconds();

            if (options.AbsoluteExpirationRelativeToNow.HasValue)
            {
                var relativeSeconds = (long)
                    Math.Ceiling(options.AbsoluteExpirationRelativeToNow.Value.TotalSeconds);
                absoluteExpiration = absoluteExpiration.HasValue
                    ? Math.Min(absoluteExpiration.Value, now + relativeSeconds)
                    : now + relativeSeconds;
            }

            var slidingExpiration = options.SlidingExpiration.HasValue
                ? (long?)Math.Ceiling(options.SlidingExpiration.Value.TotalSeconds)
                : null;

            return (now, absoluteExpiration, slidingExpiration);
        }

        private static bool IsExpired(
            long now,
            long? absoluteExpiration,
            long? slidingExpiration,
            long lastAccess
        )
        {
            if (now > absoluteExpiration)
            {
                return true;
            }

            return slidingExpiration.HasValue && now > lastAccess + slidingExpiration.Value;
        }

        private static (
            byte[] Value,
            long? AbsoluteExpiration,
            long? SlidingExpiration,
            long LastAccess
        )? ReadEntry(SqliteConnection connection, string key, DbTransaction? transaction)
        {
            using var cmd = connection.CreateCommand();
            cmd.Transaction = (SqliteTransaction?)transaction;
            cmd.CommandText =
                "SELECT value, absolute_expiration, sliding_expiration, last_access FROM cache WHERE key = @key";
            cmd.Parameters.AddWithValue("@key", key);
            using var reader = cmd.ExecuteReader();
            if (!reader.Read())
            {
                return null;
            }

            var value = (byte[])reader[0];
            var absoluteExpiration = reader.IsDBNull(1) ? (long?)null : reader.GetInt64(1);
            var slidingExpiration = reader.IsDBNull(2) ? (long?)null : reader.GetInt64(2);
            var lastAccess = reader.GetInt64(3);
            return (value, absoluteExpiration, slidingExpiration, lastAccess);
        }

        private static async Task<(
            byte[] Value,
            long? AbsoluteExpiration,
            long? SlidingExpiration,
            long LastAccess
        )?> ReadEntryAsync(
            SqliteConnection connection,
            string key,
            DbTransaction? transaction,
            CancellationToken token
        )
        {
            await using var cmd = connection.CreateCommand();
            cmd.Transaction = (SqliteTransaction?)transaction;
            cmd.CommandText =
                "SELECT value, absolute_expiration, sliding_expiration, last_access FROM cache WHERE key = @key";
            cmd.Parameters.AddWithValue("@key", key);
            await using var reader = await cmd.ExecuteReaderAsync(token);
            if (!await reader.ReadAsync(token))
            {
                return null;
            }

            var value = (byte[])reader[0];
            var absoluteExpiration = reader.IsDBNull(1) ? (long?)null : reader.GetInt64(1);
            var slidingExpiration = reader.IsDBNull(2) ? (long?)null : reader.GetInt64(2);
            var lastAccess = reader.GetInt64(3);
            return (value, absoluteExpiration, slidingExpiration, lastAccess);
        }

        private static void DeleteEntry(
            SqliteConnection connection,
            string key,
            DbTransaction? transaction
        )
        {
            using var cmd = connection.CreateCommand();
            cmd.Transaction = (SqliteTransaction?)transaction;
            cmd.CommandText = "DELETE FROM cache WHERE key = @key";
            cmd.Parameters.AddWithValue("@key", key);
            cmd.ExecuteNonQuery();
        }

        private static async Task DeleteEntryAsync(
            SqliteConnection connection,
            string key,
            DbTransaction? transaction,
            CancellationToken token
        )
        {
            await using var cmd = connection.CreateCommand();
            cmd.Transaction = (SqliteTransaction?)transaction;
            cmd.CommandText = "DELETE FROM cache WHERE key = @key";
            cmd.Parameters.AddWithValue("@key", key);
            await cmd.ExecuteNonQueryAsync(token);
        }

        private static void UpdateLastAccess(
            SqliteConnection connection,
            string key,
            long now,
            DbTransaction? transaction
        )
        {
            using var cmd = connection.CreateCommand();
            cmd.Transaction = (SqliteTransaction?)transaction;
            cmd.CommandText = "UPDATE cache SET last_access = @now WHERE key = @key";
            cmd.Parameters.AddWithValue("@now", now);
            cmd.Parameters.AddWithValue("@key", key);
            cmd.ExecuteNonQuery();
        }

        private static async Task UpdateLastAccessAsync(
            SqliteConnection connection,
            string key,
            long now,
            DbTransaction? transaction,
            CancellationToken token
        )
        {
            await using var cmd = connection.CreateCommand();
            cmd.Transaction = (SqliteTransaction?)transaction;
            cmd.CommandText = "UPDATE cache SET last_access = @now WHERE key = @key";
            cmd.Parameters.AddWithValue("@now", now);
            cmd.Parameters.AddWithValue("@key", key);
            await cmd.ExecuteNonQueryAsync(token);
        }
    }
}
